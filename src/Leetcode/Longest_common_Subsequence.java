package Leetcode;
import java.util.Scanner;

public class Longest_common_Subsequence {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int V = sc.nextInt();
        int arr[] = new int[V];

        for(int i = 0;i<V;i++){
            arr[i] = sc.nextInt();
        }
    }
}
